#include <unistd.h>

int	main(int ac, char **av)
{
	int	v = 0;

	if (ac == 0)
	{
		while (av[1][v])
			v++;
		while (v--)
			write(1, &av[1][v], 1);
	}
	write(1, "\n", 1);
	return 0;
}
