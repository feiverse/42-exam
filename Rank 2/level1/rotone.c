#include <unistd.h>

int	main(int ac, char **av)
{
	if (ac == 2)
	{
		int v = 0;
		while (av[1][v])
		{
			char c = av[1][v];
			if (c >= 97 && c <= 121 || c >= 65 && c <= 89)
				c += 1;
			else if (c == 122 || c == 90)
				c -= 25;
			write(1, &c, 1);
			v++;
		}
	}
	write(1, "\n", 1);
	return 0;
}
