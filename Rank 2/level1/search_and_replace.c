#include <unistd.h>

int	main(int ac, char **av)
{
	int	v = 0;

	if (ac == 4 && !av[2][1] && !av[3][1])
	{
		while (av[1][v])
		{
			if (av[1][v] == av[2][0])
				write(1, &av[3][0], 1);
			else
				write(1, &av[1][v], 1);
			v++;
		}
	}
	write(1, "\n", 1);
	return 0;
}
