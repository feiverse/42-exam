#include <unistd.h>


void	alpha_mirror(char *s)
{
	char c;
	
	while (*s)
	{
		if (*s >= 97 && *s <= 122)
			c = 122 - (*s - 97);
		else if (*s >= 65 && *s <= 90)
			c = 90 - (*s - 65);
		else
			c = *s;
		write(1, &c,1);
		s++;
	}
}

int	main(int ac, char **av)
{
	if (ac == 2)
		alpha_mirror(av[1]);
	write(1, "\n", 1);
	return 0;
}
