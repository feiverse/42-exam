#include <unistd.h>

void camel_to_snake(char *s) 
{
    int v = 0;
    
    while (s[v]) 
    {
        if (s[v] >= 65 && s[v] <= 90) 
        {
            write(1, "_", 1);
            s[v] += 32;
        }
        write(1, &s[v], 1);
        v++;
    }
}

int main(int ac, char **av) 
{
    if (ac == 2) 
        camel_to_snake(av[1]);
    write(1, "\n", 1);
    return 0;
}
