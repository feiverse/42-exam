#include <unistd.h>

void wdmatch(char *s1, char *s2) 
{
	char	*v = s1;
	
    while (*s2 && *s1)
        if (*s1 == *s2++)
            s1++;

    if (!*s1)
        while (*v)
            write(1, v++, 1);
}

int main(int ac, char **av) 
{
    if (ac == 3)
        wdmatch(av[1], av[2]);
    write(1, "\n", 1);
    return 0;
}