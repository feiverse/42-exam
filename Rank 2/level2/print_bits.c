#include <unistd.h>

void	print_bits(unsigned char octet)
{
	int o = 8;
	while (o--)
	{
		if ((octet >> o) & 1)
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
}


/* test */
int	main(void)
{
	print_bits(2);  // 00000010
	return 0;
}
