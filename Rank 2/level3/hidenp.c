#include <unistd.h>

int	main(int ac, char **av)
{
	if (ac == 3)
	{
		char *x = av[1], *y = av[2];
		
		while (*y && *x)
			if (*y++ == *x)
				x++;
		write(1, (*x ? "0\n" : "1\n"), 2);
	}
	else
		write(1, "\n", 1);
	return 0;
}
